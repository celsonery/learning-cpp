#include <iostream>

int main() {
    int i{1};
    float f{1.0};
    char c{'a'};
    double nu{1.0};

    std::cout << "Int: " << i << std::endl;
    std::cout << "Float: " << f << std::endl;
    std::cout << "Char: " << c << std::endl;
    std::cout << "Double: " << nu << std::endl;

    return 0;
}
